# Arithmetic operator(+,-,*,/,%,**,//,)



num1=10
num2=2

addition_result=num1+num2
print(f"{num1}+{num2}={addition_result}")

subtraction_result=num1-num2
print(f"{num1}-{num2}={subtraction_result}")

multi_result=num1*num2
print(f"{num1}*{num2}={multi_result}")

div_result=num1/num2
print(f"{num1}/{num2}={div_result}")

modulus_result=num1%num2
print(f"{num1}%{num2}={modulus_result}")

exponent_result=num1**num2
print(f"{num1}**{num2}={exponent_result}")

floor_div_result=num1//num2
print(f"{num1}//{num2}={floor_div_result}")